const removeModal = () => {
    var openDivModal = document.getElementById('openDivModal')

    if (openDivModal.style.display = 'none') {
        openDivModal.style.display = 'flex'
    }
}

const closeModal = () => {
    var openDivModal = document.getElementById('openDivModal')

    if (openDivModal.style.display = 'flex') {
        openDivModal.style.display = 'none'
    }
}

